package com.example.school.samplegame;

import android.graphics.Color;

public class Circle {
    float x;
    float y;
    float radius;
    int color;

    Circle(float x, float y, int color, float radius) {
        this.x = x;
        this.y = y;
        this.color = color;
        this.radius = radius;
    }
}
